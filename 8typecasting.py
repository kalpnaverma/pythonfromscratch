'''typecasting  is used to convert one data type into another
types-1 implicit:it is done by python intereter  lower order value is converted into high order value  without loss of data 
     -2explicit:we have to do it manually to get the desire out it converts the higher reder value in smaller one 
'''
# implicit typecasting example
a=23.4
b=10
print(a+b)# output 33.3 from  lower(int) to higher(float) conversion


#  explicit typecasting example 
a =23.4
b=10
print(int(a+b))#  output is 33  as we converted manually, interpreter didnot perform  it


